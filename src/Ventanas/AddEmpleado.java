    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        // TODO add your handling code here:
        Empleado[] a = (Empleado[]) Archivos.leerFichero(dirr);
        boolean key = false;
        for (int i = 0; i < a.length; i++) {
            key = gCodigoID.getText().equals(a[i].getCodigoID());
            if (key) {
                JOptionPane.showMessageDialog(null, "El Código pertenece a otro Empleado");
                break;
            }
        }
        if (!key) {
            if (!((gCodigoID.getText().equals(""))
                    || (gIdentificacion.getText().equals(""))
                    || (gNombre.getText().equals(""))
                    || (gDireccion.getText().equals(""))
                    || (gTelefono.getText().equals(""))
                    || (gSueldo.getText().equals("")))) {
                Empleado nuevo = new Empleado(gCodigoID.getText(), gIdentificacion.getText(), gNombre.getText(), gDireccion.getText(), gTelefono.getText(),
                        Double.parseDouble(gSueldo.getText()));
                Archivos.escribirFichero(dirr, Empleado.agregarEmpleado(a, nuevo));
                JOptionPane.showMessageDialog(null, "AGREGADO CON ÉXITO!");
            }
        }
    }//GEN-LAST:event_botonAgregarActionPerformed

