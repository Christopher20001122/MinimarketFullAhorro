    private void botonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarActionPerformed
        // TODO add your handling code here:
        Empleado[] a = (Empleado[]) Archivos.leerFichero(dirr);
        Empleado nuevo = Empleado.buscarEmpleado(a, gCodigoID.getText());
        if (nuevo == null) {

            JOptionPane.showMessageDialog(null, "Empleado NO encontrado");
        } else {
            if (!((gCodigoID.getText().equals(""))
                    || (gIdentificacion.getText().equals(""))
                    || (gNombre.getText().equals(""))
                    || (gDireccion.getText().equals(""))
                    || (gTelefono.getText().equals(""))
                    || (gSueldo.getText().equals("")))) {
                Empleado[] b = Empleado.eliminarEmpleado(a, gCodigoID.getText());
                Empleado[] c = Empleado.agregarEmpleado(b,
                        new Empleado(gCodigoID.getText(), gIdentificacion.getText(), gNombre.getText(), gDireccion.getText(), gTelefono.getText(),
                                Double.parseDouble(gSueldo.getText())));
                Archivos.escribirFichero(dirr, c);
                JOptionPane.showMessageDialog(null, "ACTUALIZADO CON ÉXITO!");
            } else {
                JOptionPane.showMessageDialog(null, "CAMPOS VACIOS, NO se puede Actualizar");
            }
        }
    }//GEN-LAST:event_botonActualizarActionPerformed

