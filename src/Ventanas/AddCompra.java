    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        // TODO add your handling code here:
        Compras[] a = (Compras[]) Archivos.leerFichero(dirr);
        boolean key = gSecuencia.getText().equals("");
        Compras nuevo = Compras.buscarCompras(a, (key) ? 0 : Integer.parseInt(gSecuencia.getText()));
        if (nuevo == null) {
            JOptionPane.showMessageDialog(null, "Compra NO ENCONTRADA");
        } else {
            gProducto.setText(nuevo.getProdAdquiridos());
            gSecuencia.setText("" + nuevo.getCodSecuencia());
            gPrecio.setText("" + nuevo.getPrecioUnit());
            gCantidad.setText("" + nuevo.getCantProductos());
        }
    }//GEN-LAST:event_botonBuscarActionPerformed

    private void botonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBorrarActionPerformed
        // TODO add your handling code here:
        Compras[] a = (Compras[]) Archivos.leerFichero(dirr);
        boolean key = gSecuencia.getText().equals("");
        Compras nuevo = Compras.buscarCompras(a, (key) ? 0 : Integer.parseInt(gSecuencia.getText()));
        if (nuevo == null) {
            JOptionPane.showMessageDialog(null, "COMPRA A ELIMINAR NO EXISTE");
        } else {
            Compras[] b = Compras.eliminarCompras(a, Integer.parseInt(gSecuencia.getText()));
            Archivos.escribirFichero(dirr, b);
            JOptionPane.showMessageDialog(null, "ELIMINADO CON ÉXITO!");
        }
    }//GEN-LAST:event_botonBorrarActionPerformed

