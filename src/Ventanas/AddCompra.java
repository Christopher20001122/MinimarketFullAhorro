    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Compras[] a = (Compras[]) Archivos.leerFichero(dirr);
        boolean key = false;
        for (int i = 0; i < a.length; i++) {
            key = gSecuencia.getText().equals(a[i].getCodSecuencia());
            if (key) {
                JOptionPane.showMessageDialog(null, "El # de Secuencia ya pertenece a una Compra");
                break;
            }
        }
        if (!key) {
            if (!((gSecuencia.getText().equals(""))
                    || (gProducto.getText().equals(""))
                    || (gPrecio.getText().equals(""))
                    || (gCantidad.getText().equals("")))) {
                Producto[] listaProductos = (Producto[]) Archivos.leerFichero("Productos.txt");
                Producto producto = Producto.buscarProductos(listaProductos, gProducto.getText());
                Compras nuevo = new Compras(Integer.parseInt(gSecuencia.getText()), fecha, gProducto.getText(),
                        Integer.parseInt(gCantidad.getText()), Double.parseDouble(gPrecio.getText()));
                Kardex[] kardex = (Kardex[]) Archivos.leerFichero("Kardex.txt");
                Kardex registro = new Kardex("C" + gSecuencia.getText(), fecha,
                        Integer.parseInt(gCantidad.getText()), producto.getPrecio() * Integer.parseInt(gCantidad.getText()), "Ingreso");
                producto.setStock(producto.getStock() + Integer.parseInt(gCantidad.getText()));
                producto.setPrecio(Double.parseDouble(gPrecio.getText()));
                Archivos.escribirFichero("Productos.txt", Producto.actualizarProductos(listaProductos, producto));
                Archivos.escribirFichero("Kardex.txt", Kardex.agregarKardex(kardex, registro));
                nuevo.setProdAdquiridos(producto.getNombre());
                Archivos.escribirFichero(dirr, Compras.agregarCompras(a, nuevo));
                JOptionPane.showMessageDialog(null, "AGREGADO CON ÉXITO");
            } else {
                JOptionPane.showMessageDialog(null, "CAMPOS VACIOS!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

