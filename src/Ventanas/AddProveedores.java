    private void botonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBorrarActionPerformed
        // TODO add your handling code here:
        Proveedores[] a = (Proveedores[]) Archivos.leerFichero(dirr);
        Proveedores nuevo = Proveedores.buscarProveedores(a, gIdentificacion.getText());
        if (nuevo == null) {
            JOptionPane.showMessageDialog(null, "No se encontro el Proveedor a eliminar");
        } else {
            Proveedores[] b = Proveedores.eliminarProveedores(a, gIdentificacion.getText());
            Archivos.escribirFichero(dirr, b);
            JOptionPane.showMessageDialog(null, "Se elimino con exito");
        }
    }//GEN-LAST:event_botonBorrarActionPerformed

    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        // TODO add your handling code here:
        Proveedores[] a = (Proveedores[]) Archivos.leerFichero(dirr);
        boolean key = false;
        for (int i = 0; i < a.length; i++) {
            key = gIdentificacion.getText().equals(a[i].getIdentificacion());
            if (key) {
                JOptionPane.showMessageDialog(null, "Un Proveedor posse esta identificaciÃ³n");
                break;
            }
        }
        if (!key) {
            if (!((gIdentificacion.getText().equals(""))
                    || (gNombre.getText().equals(""))
                    || (gDireccion.getText().equals(""))
                    || (gTelefono.getText().equals(""))
                    || (gEmail.getText().equals("")))) {

                Proveedores nuevo = new Proveedores(gIdentificacion.getText(), gNombre.getText(),
                        gDireccion.getText(), gTelefono.getText(), gEmail.getText());
                Archivos.escribirFichero(dirr, Proveedores.agregarProveedores(a, nuevo));
                JOptionPane.showMessageDialog(null, "Se agrego con exito");
            }
        }
    }//GEN-LAST:event_botonAgregarActionPerformed

