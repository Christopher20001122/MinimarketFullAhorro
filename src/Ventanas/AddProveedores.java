    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        // TODO add your handling code here:
        Proveedores[] a = (Proveedores[]) Archivos.leerFichero(dirr);
        Proveedores nuevo = Proveedores.buscarProveedores(a, gIdentificacion.getText());
        if (nuevo == null) {
            JOptionPane.showMessageDialog(null, "No se encontro el Proveedor");
        } else {
            gIdentificacion.setText(nuevo.getIdentificacion());
            gNombre.setText(nuevo.getNombre());
            gDireccion.setText(nuevo.getDireccion());
            gTelefono.setText("" + nuevo.getTelefono());
            gEmail.setText("" + nuevo.getEmail());
        }
    }//GEN-LAST:event_botonBuscarActionPerformed

    private void botonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarActionPerformed
        // TODO add your handling code here:
        Proveedores[] a = (Proveedores[]) Archivos.leerFichero(dirr);
        Proveedores nuevo = Proveedores.buscarProveedores(a, gIdentificacion.getText());
        if (nuevo == null) {

            JOptionPane.showMessageDialog(null, "No se encontro el Proveedor");
        } else {
            if (!((gIdentificacion.getText().equals(""))
                    || (gNombre.getText().equals(""))
                    || (gDireccion.getText().equals(""))
                    || (gTelefono.getText().equals(""))
                    || (gEmail.getText().equals("")))) {
                Proveedores[] b = Proveedores.eliminarProveedores(a, gIdentificacion.getText());
                Proveedores[] c = Proveedores.agregarProveedores(b,
                        new Proveedores(gIdentificacion.getText(), gNombre.getText(),
                                gDireccion.getText(), gTelefono.getText(), gEmail.getText()));
                Archivos.escribirFichero(dirr, c);
                JOptionPane.showMessageDialog(null, "Se Actualizo con exito");
            } else {
                JOptionPane.showMessageDialog(null, "NO se puede Actualizar Campos Vacios");
            }

        }
    }//GEN-LAST:event_botonActualizarActionPerformed

    private void gIdentificacionKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_gIdentificacionKeyTyped
        // TODO add your handling code here:
        char key = evt.getKeyChar();
        if (key < '0' || key > '9') {
            evt.consume();
        }
        if (gIdentificacion.getText().length() >= 20) {
            evt.consume();
        }
    }//GEN-LAST:event_gIdentificacionKeyTyped

    private void gNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_gNombreKeyTyped
        // TODO add your handling code here:
        char key = evt.getKeyChar();
        if ((key < 'a' || key > 'z') && (key < 'A' || key > 'Z') && (key != ' ')) {
            evt.consume();
        }
        if (gNombre.getText().length() >= 20) {
            evt.consume();
        }
    }//GEN-LAST:event_gNombreKeyTyped

